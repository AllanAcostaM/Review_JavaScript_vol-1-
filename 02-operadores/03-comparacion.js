
// Operadores Relacionales
let a = 5;
console.log(a > 5); // Mayor que
console.log(a >= 5); // Mayor o igual que
console.log(a < 5);  // Menor que
console.log(a <= 5); // Menor o igual que

// Operadores de Igualdad

let b = 10
console.log(b == 10); // Igual que (No distingue tipos ´==´)
console.log(b === '10'); // Igual que (Si distingue los tipos de datos ´===´)
console.log(b != 10); // Diferente que (No distingue tipos ´!=´)
console.log(b !== '10'); // Diferente que (Si distingue tipos ´!==´)


